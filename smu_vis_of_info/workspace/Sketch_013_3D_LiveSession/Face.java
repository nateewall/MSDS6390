class Face
{
  private Vertex[] vertices;
  private UV[] uvs;
  
  public Face(Vertex[] _vertices)
  {
    vertices = _vertices;
  }
  
  public Face()
  {
    vertices = new Vertex[3];
  }
  
  public Vertex[] getVertices()
  {
    return vertices;
  }
  
  public void setUVs(UV[] _uvs)
  {
    uvs = _uvs;
  }
  
  public UV[] getUVs()
  {
    return uvs;
  }
}